public class ForLoop {

	// For Loop ==> When we know the total number of iterations before itself.

	// Syntax: (condition-to-start ; condition-to-end; increment/decrement-interval)
	// {
	// statements
	// }

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(i+"Bharath");
		}

	}

}
